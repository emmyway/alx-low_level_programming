#include "calc.h"

/**
 * op_sub - subtract two values
 * @a: first value
 * @b: second value
 * Return: difference of values
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_prod - product two values
 * @a: first value
 * @b: second value
 * Return: product of values
 */

int op_prod(int a, int b)
{
	return (a * b);
}

/**
 * op_div - divides two values
 * @a: first value
 * @b: second value
 * Return: result of division of values
 */

int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modular two values
 * @a: first value
 * @b: second value
 * Return: mod of values
 */

int op_mod(int a, int b)
{
	return (a % b);
}

/**
 * op_add - add two values
 * @a: first value
 * @b: second value
 * Return: sum of values
 */

int op_add(int a, int b)
{
	return (a + b);
}
